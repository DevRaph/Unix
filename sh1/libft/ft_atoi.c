#include "libft.h"

int		ft_atoi(const char *s)
{
	int nb;
	int negative;

	negative = 1;
	while (ft_isspace(*s))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			negative = -1;
		s++;
	}
	nb = 0;
	while (*s >= '0' && *s <= '9')
	{
		nb *= 10;
		nb += (*s - '0');
		s++;
	}
	return (nb * negative);
}
