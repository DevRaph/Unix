#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	char *ptr;

	if (s == NULL)
		return (NULL);
	ptr = (char *)s;
	while (*ptr)
	{
		if (*ptr == c)
			return (ptr);
		ptr++;
	}
	if (*ptr == c)
		return (ptr);
	return (NULL);
}
